package jh.javabookcodes;

public class OperationTest1 {
    public static void main(String[] args) {

        int result = 0;
        int var_int1 = 10;
        int var_int2 = 2;

        // +연산
        result = var_int1 + var_int2;
        System.out.println("var_int1 + var_int2 = " + result);

        // -연산
        result = var_int1 - var_int2;
        System.out.println("var_int1 - var_int2 = " + result);

        // *연산
        result = var_int1 * var_int2;
        System.out.println("var_int1 * var_int2 = " + result);

        // / 연산
        result = var_int1 / var_int2;
        System.out.println("var_int1 / var_int2 = " + result);

        // % 연산
        result = var_int1 % var_int2;
        System.out.println("var_int1 % var_int2 = " + result);

    }
}
